#include "libft.h"

void ft_striteri (char *s, void (*f) (unsigned uint, char *)) {
	unsigned char i;

	i = 0;
	if (s != NULL && f != NULL) {
		while (*s != '\0') {
			(*f)(i, s);
			i++;
			s++;
		}
	}
}
